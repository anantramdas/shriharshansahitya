function bookTemplate(folderName) {
    var title = booksList[folderName]['title'];
    var intro = booksList[folderName]['intro'];
    var introAuthor = booksList[folderName]['introAuthor'];
    var pageCount = booksList[folderName]['pageCount'];
    var miniBook = booksList[folderName]['miniBook'] || 0;
    var bookSize = '';
    var isMini = miniBook == 1 ? ' small' : '';
    if (pageCount > 300) {
        bookSize = ' large';
    } else if (pageCount > 90) {
        bookSize = ' medium';
    }
    return `<div class="a-box" onmouseenter="bookActive(this)" onmouseleave="bookInactive(this)">
    <div class="book${isMini}">
        <div class="book__wrapper${bookSize}"></div>
        <img class="book__cover" alt="${title}" src="assets/${folderName}.jpg" />
    </div>
    <div class="about-book">
        <p class="book-title">${title}</p>
        <div class="book-intro">
            ${intro}
            <div class="book-intro-author"><span class="page-count">(कुल पृष्ठ : ${pageCount})</span>${introAuthor}</div>
        </div>
        <div class="button-panel">
            <a class="button" onclick="loadPdf('${folderName}');">पुस्तक पढ़ें</a>
            <a class="button">डाउनलोड करें</a>
        </div>
    </div>
</div>`;
}